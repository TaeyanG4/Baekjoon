{
    class MainApp
    {
        static void solveNow()
        {
            String[] input = Console.ReadLine().Split();
            int a = int.Parse(input[0]);
            for (int i = 0; i < a; i++)
            {
                Console.ReadLine();
            }
            Console.WriteLine("비와이");
        }
        static void Main(string[] args)
        {
            solveNow();
        }
    }
}